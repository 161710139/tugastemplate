<p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspKodaline adalah grup musik asal Irlandia. Grup musik ini dikenal sebagai 21 Demands, sebelum diganti menjadi Kodaline pada tahun 2012.</p>

<p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspSteve Garrigan dan Mark Prendergast dibesarkan di Swords, Dublin dan telah berteman sejak kecil. Mereka akhirnya bermusik bersama hingga Vinny May, yang juga tinggal di Swords, bergabung. Pada bulan Januari tahun 2012, bassist Jason Boland direkrut sebagai anggota.</p>

<h4>Karier</h4>
<b>2005–11: 21 Demands</b>
<p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspSebagai 21 Demands, grup musik ini mulai dikenal pada bulan November 2007, ketika itu mereka ikut kontes pencarian bakat You're a Star yang tayang di RTÉ dan berhasil menjadi runner-up. Pada 3 Maret 2007, 21 Demands merilis singel "Give Me a Minute" yang berhasil menduduk peringkat pertama tangga lagu Irlandia.</p>

<b>2012–14: In a Perfect World</b>
<p>
&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspGrup musik ini kemudian merilis album mini debut, The Kodaline EP pada 7 September 2012.[2] Pada 9 Desember 2012, BBC mengumumkan bahwa Kodaline masuk dalam nominasi untuk Sound of... 2013, sebuah polling tentang musisi baru yang menjanjikan.[3]
</p>
<p>
&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspPada 17 Juni 2013, Kodaline merilis album debut mereka, In a Perfect World.[4] Album ini didukung oleh beberapa singel, termasuk "High Hopes" dan "All I Want". Pada tahun 2014, "All I Want" dijadikan soundtrack film drama romansa, The Fault in Our Stars. Pada tahun yang sama, lagu mereka yang lain, "Pray" dan "High Hopes" dimasukkan dalam trailer film, Horns dan Love, Rosie.[5]
</p>

<b>2014–17: Coming Up for Air</b>
<p>
&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspPada 9 Februari 2015, grup musik ini merilis album kedua mereka, Coming Up for Air, didukung oleh singel, "Honest" dan "The One". Kodaline pun tampil untuk pertama kalinya di Indonesia dalam Lalala Festival yang diselenggarakan di Cikole, Bandung.[6]
</p>

<b>2017–sekarang: Album ketiga</b>
<p>
&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspSingel utama dari album ketiga berjudul "Brother" dirilis pada 23 Juni 2017. Kodaline tampil untuk kedua kalinya di Indonesia pada festival musik, We the Fest.[7]
</p>

